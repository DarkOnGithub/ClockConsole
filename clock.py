from time import sleep
import datetime
import os
digit = {
    "a":True,
    "1":["      ██","      ██","      ██","      ██","      ██"],
    "2":["████████","      ██","████████","██      ","████████"],
    "3":["████████","      ██","████████","      ██","████████"],
    "4":["██    ██","██    ██","████████","      ██","      ██"],
    "5":["████████","██      ","████████","      ██","████████"],
    "6":["████████","██      ","████████","██    ██","████████"],
    "7":["████████","      ██","      ██","      ██","      ██"],
    "8":["████████","██    ██","████████","██    ██","████████"],
    "9":["████████","██    ██","████████","      ██","      ██"],
    "0":["████████","██    ██","██    ██","██    ██","████████"],
    ":":["   ██   ","        ","        ","        ","   ██   "]
}
while True:
    if digit["a"]:digit["a"]=False    
    else:print("\033[F"*5)
    clock=""
    for x in range(5):
        string = ""
        for i in str(datetime.datetime.now().strftime("%H:%M:%S:%f")[:-3]):
            string += digit[i][x]+" "
        for i in range(os.get_terminal_size().columns - len(string)):
            string+=" "
        clock+=string
    print(clock,end="")
    sleep(0.01)